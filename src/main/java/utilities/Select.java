package utilities;

import org.openqa.selenium.By;

public class Select {

	// *********Select by Visible Text*********
	public static void selectByVisibleText(By elementBy, String visibleTxt) {
		Select.selectByVisibleText(elementBy, visibleTxt);
	}

	// *********Select by Value*********
	public static void selectByValue(By elementBy, String value) {
		Select.selectByValue(elementBy, value);
	}

	// *********Select by Index*********
	public static void selectByIndex(By elementBy, int value) {
		Select.selectByIndex(elementBy, value);
	}
}
